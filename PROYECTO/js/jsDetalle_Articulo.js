function Comprar(){
    var x = document.getElementById("ShowValoration");
    if(x.style.display === "block"){
        x.style.display = "none";
    }else{
        x.style.display = "block";
    }

    var x = document.getElementById("ShowToComment");
    if(x.style.display === "block"){
        x.style.display = "none";
    }else{
        x.style.display = "block";
    }
}